function generateUsers(): UserRecord[] {
  const extraRounds = 4;
  const generated = Array.from({ length: extraRounds }).flatMap((_, index) => {
    const offset = (index + 1) * baseUsers.length;
    return baseUsers.map((user, userIndex) => ({
      ...user,
      id: `USR-${String(offset + userIndex + 1).padStart(3, "0")}`,
      email: user.email.replace("@", `+${index + 1}@`),
      joinedAt: user.joinedAt,
      lastActive: user.lastActive,
    }));
  });

  return [...baseUsers, ...generated];
}

export const users: UserRecord[] = generateUsers();
